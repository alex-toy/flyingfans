
<section class="encart">
  Passioné de pilotage, je connais les difficultés d'entreprendre un vol en zone inconnue pour la première fois. L'un des risques majeurs pour un pilote est de se perdre, avec les conséquences tragiques que cela peut avoir : gestion du carburant totalement faussée, risque de pénétrer dans un espace aérien contrôlé sans s'en rendre compte... Bien sûr, nous avons tous des GPS mais ces derniers, bien que de formidables outils pour le pilote, peuvent également apporter leur lot de stress supplémentaire notamment en cas de panne ou de perte de réseau.
  Quoi de mieux que d'avoir son propre GPS dans la tête! Avec ce blog je souhaite que tous les pilotes puissent prévisualiser les vols aux points caractéristiques 
</section>




